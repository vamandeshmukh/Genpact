// some static properties of the Number class

// Number.MIN_VALUE is minimum positive floating-point value
console.log( Number.MIN_VALUE, Number.MAX_VALUE );

// no loss of precision occurs when integers fall in this range. Hence equality comparison is safe for these numbers (available in ES6+)
console.log( Number.MIN_SAFE_INTEGER, Number.MAX_SAFE_INTEGER );

console.log( Number.POSITIVE_INFINITY, Number.NEGATIVE_INFINITY );