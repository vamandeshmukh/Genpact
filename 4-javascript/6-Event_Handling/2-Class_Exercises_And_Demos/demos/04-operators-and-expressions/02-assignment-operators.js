/**
 * Assignment operators
 * They work as in most other languages
 */

var a = 3;	// assignment operator (not suprising!)
a += 10;
console.log( 'After a += 10, a = ', a );
console.log( 'a -=6: ', a -= 6 );	// note that the value of the assignment expression is same as that of a
console.log( 'a *=3: ', a *= 3 );
console.log( 'a /=2: ', a /= 2 );
console.log( 'a %= 4: ', a %=4  );	// note this - 2.5